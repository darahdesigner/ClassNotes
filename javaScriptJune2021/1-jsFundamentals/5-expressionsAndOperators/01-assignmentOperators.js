//    Assignment operator
let x = 20;
// additiona assignment operator
x += 1; //x = x + 1
console.log(x);

// multiplication assignment operator
x *= 2; //x= = x * 2
console.log(x);

// division assignment operator
x/=4; //x = x /4
console.log(x);

// remainder (modulus) assignment operator
x %= 2
console.log(x);

